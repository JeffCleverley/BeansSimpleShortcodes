Shortcode displays an admin login/logout link depending on whether a user is logged in or out.
Supported attributes are <strong>before-login</strong>, <strong>before-logout</strong>, <strong>after-login</strong>, <strong>after-logout</strong>,
<strong>login-text</strong>, <strong>logout-text</strong>, <strong>login-redirect</strong>, and <strong>logout-redirect</strong>:



