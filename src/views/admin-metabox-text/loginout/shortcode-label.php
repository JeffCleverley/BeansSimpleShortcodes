Shortcode displays an admin login/logout link depending on whether a user is logged in or out. Link and attributes are displayed inside a &lt;span&gt; element.</br>
Supported attributes are <strong>before-login</strong>, <strong>before-logout</strong>, <strong>after-login</strong>, <strong>after-logout</strong>,
<strong>login-text</strong>, <strong>logout-text</strong>, <strong>login-redirect</strong>, <strong>logout-redirect</strong>,</br> <strong>span-class</strong>, <strong>span-style</strong>, <strong>link-class</strong>, and <strong>link-style</strong>:



